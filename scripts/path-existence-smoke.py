#!/usr/bin/env python3
"""path-existence-smoke - assert every backtick-quoted path in operator-
facing docs resolves to a real file or directory.

Two iterations of the path-existence audit shape (commits 5e01954,
385c867) caught 7 broken file-path references across 9 files
(crypto/audit.py vs audit_log.py; goldens/...
vs goldens/.../expected-findings.json; etc.). Each was a doc
claiming a path exists that doesn't - a SANS judge or contributor
following the citation would hit a 404. This smoke locks the
audit so future contributors can't silently re-introduce the same
shape.

For each operator-facing doc in the SCAN_LIST, extract every
backtick-quoted string of the form ``path/with/at/least/one/slash``
and check that it resolves to a real file or directory. Resolution
is tried in order: relative to the doc's own dir; relative to repo
root; for service READMEs, also relative to the service's package
dir (e.g. services/agent/README.md mentioning ``crypto/audit_log.py``
should resolve at services/agent/findevil_agent/crypto/audit_log.py).

A defined ALLOW_PATTERNS list captures the false-positive shapes the
manual audits had to skip past:
  * URLs (http://, https://, mailto:)
  * MCP wire-format identifiers (tools/list, tools/call)
  * Runtime user dirs (~/.claude/, ~/hayabusa/...)
  * Install paths in decision runbooks (/usr/bin/, /usr/share/)
  * Deferred-per-A2 widget paths (apps/mcp-widgets/)
  * Dropped-per-A2 modules deliberately quoted in docs
    (findevil_agent/cli.py + 4 siblings)
  * Future-tense fixture files (fixtures/sha256sums.txt etc -
    generated by scripts/fetch-fixtures.sh at first download)
  * Conventional shorthand (`.LOG1` / `.LOG2`)

Wall-clock: ~30ms (a few hundred regex matches + Path.exists()
calls). Wired into docker/l1-compose.yml after divergence-smoke
as the 8th L1 smoke.
"""

