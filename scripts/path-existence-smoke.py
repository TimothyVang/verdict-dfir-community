DOC_SCOPED_ALLOW_PATTERNS: dict[str, tuple[re.Pattern[str], ...]] = {
    # Internal competitive research quotes GitHub repo slugs (`owner/repo`).
    # Those are external identifiers, not repo-local paths.
    "docs/competitive-analysis.md": (re.compile(r"^[A-Za-z0-9_.-]+/[A-Za-z0-9_.-]+$"),),
}

# Compile once.  PATH_RE matches any backtick-quoted token that
# contains at least one slash.
PATH_RE = re.compile(r"`([^`\s]+/[^`\s]+)`")

OK = "[OK  ]"
FAIL = "[FAIL]"


def _is_path_shape(target: str) -> bool:
    """True if target *looks* like a candidate file/dir reference.
    Filters out tool-arg shapes, glob patterns, and other obviously-
    not-a-path strings before we even try to resolve."""
    if any(c in target for c in "*?[]{}<>="):
        return False
    if "|" in target:
        return False
    # foo:bar where bar isn't a known file extension is probably
    # an arg shape (e.g. `case_id:str`, not `path:line`).
    if ":" in target and not any(
        target.endswith(ext)
        for ext in (
            ".md",
            ".py",
            ".rs",
            ".sh",
            ".toml",
            ".yml",
            ".yaml",
            ".json",
            ".txt",
            ".deb",
            ".evtx",
            ".log",
            ".jsonl",
            ".html",
            ".pdf",
            ".png",
        )
    ):
        return False
    return True


def _is_allowed(target: str, doc: str) -> bool:
    """True if target matches one of the known false-positive
    shapes the audit should skip."""
    if any(p.match(target) for p in ALLOW_PATTERNS):
        return True
    return any(p.match(target) for p in DOC_SCOPED_ALLOW_PATTERNS.get(doc, ()))


def _resolve(doc: str, target: str) -> Path | None:
    """Try resolution attempts in order. Returns the resolved
    Path if any of them exists, else None."""
    src_dir = (REPO / doc).parent
    candidates = [
        src_dir / target,
        REPO / target,
    ]
    if doc in SERVICE_PACKAGE_DIRS:
        candidates.append(REPO / SERVICE_PACKAGE_DIRS[doc] / target)
    for c in candidates:
        try:
            if c.exists():
                return c
        except OSError:
            continue
    return None


def _discover_docs() -> list[str]:
    """Return the sorted list of repo-relative doc paths to scan.
    Combines EXPLICIT_DOCS with GLOB_PATTERNS, minus GLOB_EXCLUDES.
    Glob-based so a new doc dropped under docs/, docs/runbooks/,
    agent-config/, or services/*/ is auto-gated next CI run."""
    seen: set[str] = set()
    for d in EXPLICIT_DOCS:
        if (REPO / d).exists():
            seen.add(d)
    for pat in GLOB_PATTERNS:
        for p in REPO.glob(pat):
            if not p.is_file():
                continue
            rel = p.relative_to(REPO).as_posix()
            if rel in GLOB_EXCLUDES:
                continue
            seen.add(rel)
    return sorted(seen)


def main() -> int:
    print("=" * 60)
    print("Find Evil! - path-existence-smoke")
    print("=" * 60)

    docs = _discover_docs()
    missing = []
    total_paths = 0
    for doc in docs:
        p = REPO / doc
        # _discover_docs only yields existing files; no need to
        # re-check existence here.

        text = p.read_text(encoding="utf-8")
        for m in PATH_RE.finditer(text):
            target = m.group(1).rstrip(".,;:?")
            if not _is_path_shape(target):
                continue
            if _is_allowed(target, doc):
                continue
            total_paths += 1
            resolved = _resolve(doc, target)
            if resolved is None:
                line_no = text[: m.start()].count("\n") + 1
                missing.append((f"{doc}:{line_no}", target))

    print(f"checked {total_paths} backtick-quoted paths across {len(docs)} docs")
    print()

    if missing:
        for src, target in missing:
            print(f"{FAIL} {src}: {target!r}")
        print()
        print("=" * 60)
        print(f"FAIL - {len(missing)} broken path reference(s).")
        print("To fix: either correct the path or, if it's a")
        print("legitimate exception (e.g. a runtime install path,")
        print("a deferred-per-A2 future surface, or a")
        print("documented-and-quoted dropped module), add it to")
        print("ALLOW_PATTERNS in scripts/path-existence-smoke.py")
        print("with a one-line justification comment.")
        print("=" * 60)
        return 1

    print("=" * 60)
    print(f"OK - all {total_paths} path references resolve cleanly.")
    print("=" * 60)
    return 0


if __name__ == "__main__":
    sys.exit(main())
